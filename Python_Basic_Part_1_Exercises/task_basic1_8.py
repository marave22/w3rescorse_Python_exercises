# Write a Python program to display the first and last colors from the following list.
# color_list = ["Red","Green","White" ,"Black"]

color_list = ["Red", "Green", "White", "Black"]
firstElement = color_list[0]
lastElement = color_list[len(color_list) - 1]
print("The First and Last elements of the List: ", firstElement, " ", lastElement)
